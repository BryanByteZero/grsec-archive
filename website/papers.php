							<h2 itemprop="name headline" class="excerpt-title">RBAC Tutorial</h2>

							<p>This presentation was delivered at Locaweb, a grsecurity sponsor, in October 2012. The purpose of the talk is to introduce users to RBAC as implemented in grsecurity. Freeing from the mind ancient "formal methods" with their associated outdated assumptions, I present the scope and goals of a modern access control system. The presentation also provides a real-life policy for CVS pserver, discussing some security attributes that emerge from the policy, particularly when used in combination with the other features of grsecurity.</p>

