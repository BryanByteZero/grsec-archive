							<h2 itemprop="name headline" class="excerpt-title">Linux Security in 10 Years</h2>

							<p>In the presentation I touch on a number of topics ranging from exploitation to security model theorizing to prevention. I provide a brief discussion of lessons learned from last year's exploit releases, a discussion of the real-life implications of the kernel being in the TCB, a 
							description of what grsecurity is doing right now in terms of kernel self-protection, and an outline of our ultimafor kernel self-protection.</p>

